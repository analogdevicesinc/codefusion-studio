/**
 * Extracts default values for cfs.* settings from the IDE's package.json
 * and writes them to a TypeScript module that cfs-lib can consume at runtime.
 */

import {readFileSync, writeFileSync} from 'node:fs';
import {dirname, resolve} from 'node:path';
import {fileURLToPath} from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));

const idePackageJsonPath = resolve(
	__dirname,
	'../../ide/package.json'
);
const outputPath = resolve(
	__dirname,
	'../src/resources/cfs-setting-defaults.ts'
);

const idePackageJson = JSON.parse(
	readFileSync(idePackageJsonPath, 'utf8')
);

const configurations = idePackageJson.contributes?.configuration ?? [];

const defaults = {};

for (const section of configurations) {
	if (!section.properties) continue;

	for (const [key, prop] of Object.entries(section.properties)) {
		if (key.startsWith('cfs.') && 'default' in prop) {
			defaults[key] = prop.default;
		}
	}
}

writeFileSync(
	outputPath,
	[
		'/**',
		' *',
		' * Copyright (c) 2026 Analog Devices, Inc.',
		' *',
		' * Licensed under the Apache License, Version 2.0 (the "License");',
		' * you may not use this file except in compliance with the License.',
		' *',
		' */',
		'',
		'// AUTO-GENERATED by scripts/extract-setting-defaults.js',
		'// Do not edit manually. Source of truth: packages/ide/package.json contributes.configuration.',
		'',
		`export const cfsSettingDefaults: Record<string, unknown> = ${JSON.stringify(defaults, null, 2)};`,
		''
	].join('\n')
);

console.log(
	`Extracted ${Object.keys(defaults).length} setting defaults to ${outputPath}`
);
